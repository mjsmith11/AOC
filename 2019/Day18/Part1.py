class Point:
    def __init__(self, x, y):
        self.x = x
        self.y = y
        
    def getString(self):
        return str(self.x) + "," + str(self.y)
        
    def Around(self):
        return [Point(self.x+1, self.y), Point(self.x-1,self.y), Point(self.x,self.y-1), Point(self.x,self.y+1)]
        
class ReachableKey:
    def __init__(self, key, distance, obstacles):
        self.key = key
        self.obstacles = obstacles
        self.distance = distance
        
class State:
    def __init__(self, position, ownedKeys, steps):
        self.position = position
        self.ownedKeys = ownedKeys
        self.steps = steps
        
        
