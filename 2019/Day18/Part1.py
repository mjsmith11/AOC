def ReachableKeys(lines, start, currentKeys):
    list = []
    visited = {}
    
    q = []
    s = []
    o = []
    
    q.append(start)
    s.append(0)
    o.append(0)
    
    while(len(q) > 0):
        pos = q.pop(0)
        dist = s.pop(0)
        obst = o.pop(0)
        
        if (pos.getString() in visited):
            continue
        
        visited[pos.getString()]=""
        c = lines[pos.y][pos.x]
        
        if (c=="@"):
            c="."
        
        if(c.islower()):
            list.append(ReachableKey(c, dist, obst))
            for p in pos.Around():
                q.append(p)
                s.append(dist + 1)
                o.append(obst)
        elif(c.isupper()):
            for p in pos.Around():
                q.append(p)
                s.append(dist + 1)
                o.append(obst | (2**(ord(c.lower())-ord("a"))))
        elif(c=="."):
            for p in pos.Around():
                q.append(p)
                s.append(dist + 1)
                o.append(obst)
    return list
    
def CollectKeys(map, keyPaths, numKeys):
    pos = FindPositionOf("@", map)
    currentMin = 1000000000
    
    startingSpot = pos
    
    q = []
    q.append(State(startingSpot, 0, 0))
    
    visited = {}
    finishValue = 0
    
    for i in range(numKeys):
        finishValue |= 2**i
    
    while(len(q) > 0):
        state = q.pop(0)
        valueString = str(state.position.x)+","+str(state.position.y)+","+str(state.ownedKeys)
        if (valueString in visited):
            steps = visited[valueString]
            if (steps<=state.steps):
                continue # if we've been in this state already with less steps, we don't care
            # otherwise, we update the visited so it knows
            visited[valueString] = state.steps
        else:
            visited[valueString] = state.steps
            
        if(state.ownedKeys == finishValue):
            currentMin = min(state.steps, currentMin)
            print(currentMin)
            continue
        
        for k in keyPaths[state.position.getString()]:
            ki = 2**(ord(k.key) - ord('a'))
            if ((state.ownedKeys & ki) == ki): #(
                continue
            if ((k.obstacles & state.ownedKeys) != k.obstacles):
                continue
            newOwned = state.ownedKeys | ki
            
            newPos = FindPositionOf(k.key, lines)
            q.append(State(newPos, newOwned, state.steps + k.distance))
    return currentMin
    
    
