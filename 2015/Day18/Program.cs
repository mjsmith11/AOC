		public static List<List<bool>> step(List<List<bool>> grid)
		{
			List<List<bool>> result = new List<List<bool>>();
			for (int y = 0; y < 100; y++)
			{
				List<bool> row = new List<bool>();
				for (int x = 0; x < 100; x++)
				{
					//four corners
					if ((x == 0 && y == 0) || (x == 0 && y == 99) || (x == 99 && y == 99) || (x == 99 && y == 0))
					{
						row.Add(true);
						continue;
					}
					int neighborsOn = 0;
					// up-left
					if (x > 0 && y > 0 && grid[y - 1][x - 1]) { neighborsOn++; }
					// up
					if (y > 0 && grid[y - 1][x]) { neighborsOn++; }
					// up-right
					if (x < 99 && y > 0 && grid[y - 1][x + 1]) { neighborsOn++; }
					// right
					if (x < 99 && grid[y][x + 1]) { neighborsOn++; }
					// down-right
					if (x < 99 && y < 99 && grid[y + 1][x + 1]) { neighborsOn++; }
					// down
					if (y < 99 && grid[y + 1][x]) { neighborsOn++; }
					// down-left
					if (y < 99 && x > 0 && grid[y + 1][x - 1]) { neighborsOn++; }
					// left
					if (x > 0 && grid[y][x - 1]) { neighborsOn++; }

					if (grid[y][x])
					{
						if (neighborsOn == 2 || neighborsOn == 3)
						{
							row.Add(true);
						}
						else
						{
							row.Add(false);
						}
					}
					else
					{
						if (neighborsOn == 3)
						{
							row.Add(true);
						}
						else {
							row.Add(false);
						}
					}
				}
				result.Add(row);
			}
			return result;
		}

