        static int countTrees(string[] map, int rightAmt, int downAmt) {
            int height = map.Length;
            int width = map[0].Trim().Length;

            int row = 0;
            int col = 0;
            int trees = 0;
            while(row<height) {
                if (map[row][col] == '#') { trees++; }
                
                row += downAmt;
                col += rightAmt;
                if (col>=width) { col -= width; }
            }
            return trees;
        }

