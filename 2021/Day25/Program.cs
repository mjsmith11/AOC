        static int part1() {
            string[] inp = getInput();
            int xsize = inp[0].Trim().Length;
            int ysize = inp.Length;
            char[,] grid = new char[xsize,ysize];
            for(int y=0; y<ysize; y++) {
                for(int x=0; x<xsize; x++) {
                    grid[x,y]=inp[y][x];
                }
            }

            int rounds = 0;
            while(true) { // hopefully it works and we return out of this
                char[,] afterEast = new char[xsize,ysize];
                for(int x=0; x<xsize; x++){
                    for(int y=0; y<ysize; y++) {
                        if (grid[x,y]=='v') {
                            afterEast[x,y]='v'; // southbound untouched
                        } else if(grid[x,y] == '.' && afterEast[x,y] != '>') {
                            // it's empty and we haven't moved a cucumber here
                            afterEast[x,y] = '.';
                        } else if (grid[x,y]=='>') {
                            int targetX = x + 1;
                            if (targetX>=xsize) {targetX = 0; }
                            if (grid[targetX,y]=='.') {
                                // move
                                afterEast[x,y]='.';
                                afterEast[targetX,y]='>';
                            } else {
                                // stay put
                                afterEast[x,y]='>';
                            }
                        }
                    }
                }

                char[,] afterSouth = new char[xsize,ysize];
                for(int x=0; x<xsize; x++){
                    for(int y=0; y<ysize; y++) {
                        if (afterEast[x,y]=='>') {
                            afterSouth[x,y]='>'; // eastbound untouched
                        } else if(afterEast[x,y] == '.' && afterSouth[x,y] != 'v') {
                            // it's empty and we haven't moved a cucumber here
                            afterSouth[x,y] = '.';
                        } else if (afterEast[x,y]=='v') {
                            int targetY = y + 1;
                            if (targetY>=ysize) {targetY = 0; }
                            if (afterEast[x,targetY]=='.') {
                                // move
                                afterSouth[x,y]='.';
                                afterSouth[x,targetY]='v';
                            } else {
                                // stay put
                                afterSouth[x,y]='v';
                            }
                        }
                    }
                }
                rounds++;
                if (equals(grid,afterSouth,xsize,ysize)) { return rounds; }
                grid = afterSouth;
            }

        }

        static bool equals(char[,] a, char[,] b, int xsize, int ysize) {
            for(int x=0; x<xsize; x++) {
                for(int y=0; y<ysize; y++) {
                    if(a[x,y]!=b[x,y]) {return false;}
                }
            }
            return true;
        }

