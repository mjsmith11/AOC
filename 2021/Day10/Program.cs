static int part1() {
    string[] input = getInput();
    int score = 0;
    foreach(string line in input){
        Stack<char> chunks = new Stack<char>();
        foreach(char c in line) {
            switch(c) {
                case '(':
                case '{':
                case '[':
                case '<':
                    chunks.Push(c);
                    break;
                case ')':
                    if (chunks.Pop() != '(') {
                        score += 3;
                    }
                    break;
                case ']':
                    if (chunks.Pop() != '[') {
                        score += 57;
                    }
                    break;
                case '}':
                    if (chunks.Pop() != '{') {
                        score += 1197;
                    }
                    break;
                case '>':
                    if (chunks.Pop() != '<') {
                        score += 25137;
                    }
                    break;
            }
        }
    }
    return score;
}


static long part2() {
    string[] input = getInput();
    List<long> scores = new List<long>();
    foreach(string line in input){
        Stack<char> chunks = new Stack<char>();
        bool error = false;
        foreach(char c in line) {
            switch(c) {
                case '(':
                case '{':
                case '[':
                case '<':
                    chunks.Push(c);
                    break;
                case ')':
                    if (chunks.Pop() != '(') {
                        error = true;
                    }
                    break;
                case ']':
                    if (chunks.Pop() != '[') {
                        error = true;
                    }
                    break;
                case '}':
                    if (chunks.Pop() != '{') {
                        error = true;
                    }
                    break;
                case '>':
                    if (chunks.Pop() != '<') {
                        error = true;
                    }
                    break;
            }
        }
        if (!error) {
            long score = 0;
            while (chunks.Count() > 0) {
                score *= 5;
                char c = chunks.Pop();
                switch(c) {
                    case '(':
                        score += 1;
                        break;
                    case '{':
                        score += 3;
                        break;
                    case '[':
                        score += 2;
                        break;
                    case '<':
                        score += 4;
                        break;
                }
            }
            scores.Add(score);
        }
    }
    scores = scores.OrderBy(i => i).ToList();
    return scores[scores.Count()/2];
}

